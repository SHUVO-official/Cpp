/* This code calculates the sum of three numbers. */
#include<iostream>
using namespace std;
    
int main() {
    int x,y,z,sum;
    cout<<"Enter the 1st number x: ";
    cin>>x;
    cout<<"Enter the 2nd number y: ";
    cin>>y;
    cout<<"Enter the 3rd number z: ";
    cin>>z;
    sum=x+y+z;
    cout<<"The sum of the three numbers is: "<<sum<<endl;
    return 0;
}
